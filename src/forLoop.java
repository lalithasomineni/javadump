public class forLoop {
   public static void main(String[] args) {
      int num = 1;
      for(num = 1;num <=100;num++){
        System.out.println(num);
      }
      int num2 = 10;
      int i;
      int product = 1;
      for(i = 1;i <=num2;i++){
          product = product * i;
          System.out.println(product);
      }
   } 
}
