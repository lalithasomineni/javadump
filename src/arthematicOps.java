public class arthematicOps {
    public static void main(String[] args) {
        int x = 10;
        int y = 11;
        int z = x/y;
        System.out.println(z); //0

        double d = x/y;
        System.out.println(d); //0.0

        double a = 10.0;
        double b = 11.0;
        double c = a/b;
        System.out.println(c); //0.9
    }
}
